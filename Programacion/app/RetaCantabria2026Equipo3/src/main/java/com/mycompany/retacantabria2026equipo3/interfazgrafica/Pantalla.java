private void rellenarTabla(JTable tabla){
        // Columnas
        String[] columnas = {
                "id_material",
                "nombre",
                "descripcion",
                "cantidad",
                "stock_minimo",
                "categoria",
                "estado",
                "id_ubicacion"
        };

        // Modelo
        DefaultTableModel modelo = new DefaultTableModel(columnas, 0);

        // Tabla
        tabla = new JTable(modelo);

        // Rellenar tabla
        if(!materiales.isEmpty()){
            for (Material m : materiales) {

            Object[] fila = {
                    m.getId(),
                    m.getNombre(),
                    m.getDescripcion(),
                    m.getCantidad(),
                    m.getStockMinimo(),
                    m.getCategoria(),
                    m.getEstado(),
                    m.getIdUbicacion()
            };

            modelo.addRow(fila);
        }
        }
        
}


