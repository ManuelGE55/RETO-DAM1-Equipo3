private void rellenarTablaMateriales(){
        // Columnas
        String[] columnas = {
                "id_material",
                "nombre",
                "descripcion",
                "cantidad",
                "stock_minimo",
                "categoria",
                "estado",
                "id_ubicacion"
        };

        // Modelo
        DefaultTableModel modelo = new DefaultTableModel(columnas, 0);

        // Tabla
        jTable1.setModel(modelo);

        // Rellenar tabla
        if(!materiales.isEmpty()){
            for (Material m : materiales) {

            Object[] fila = {
                    m.getId(),
                    m.getNombre(),
                    m.getDescripcion(),
                    m.getCantidad(),
                    m.getStockMinimo(),
                    m.getCategoria(),
                    m.getEstado(),
                    m.getIdUbicacion()
            };

            modelo.addRow(fila);
        }
        }
        
}

